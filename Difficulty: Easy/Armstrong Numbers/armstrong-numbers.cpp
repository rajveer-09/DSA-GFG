class Solution {
  public:
    bool armstrongNumber(int n) {
        // code here
        int digits = log10(n) + 1;
        
        int sum = 0;
        int tmp = n;
        
        while(tmp){
            sum += pow(tmp % 10, digits);
            
            tmp /= 10;
        }
        
        return sum == n;
    }
};


